public class CowsBulls {
    private int cows;
    private int bulls;
    public CowsBulls(int cows, int bulls){
        this.cows = cows;
        this.bulls = bulls;
    }
    public int getCows(){
        return cows;
    }
    public int getBulls(){
        return bulls;
    }
    public void setCows(int cows){
        this.cows = cows;
    }
    public void setBulls(int bulls){
        this.bulls = bulls;
    }
}
